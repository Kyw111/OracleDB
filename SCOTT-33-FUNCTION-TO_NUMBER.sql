-- 문자 데이터를 숫자 데이터로 변환
-- TO_NUMBER(문자열, 숫자포맷)

SELECT TO_NUMBER('12345', '999999') FROM DUAL;

SELECT '12345' + 10 FROM DUAL;
SELECT '12345' - 10 FROM DUAL;
SELECT '12345' - '-10' FROM DUAL;

SELECT TO_NUMBER('12,345', '99,999') + '10' FROM DUAL;
SELECT TO_NUMBER('12,345.12', '99,999.99') + '0.23' FROM DUAL;